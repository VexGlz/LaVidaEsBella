    private void btnCancelarSolicitudActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnCancelarSolicitudActionPerformed
        int filaSeleccionada = tablaSolicitudes.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Por favor selecciona una solicitud para cancelar.",
                    "Selección requerida",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        String idSolicitud = (String) modeloTabla.getValueAt(filaSeleccionada, 0);
        String estado = (String) modeloTabla.getValueAt(filaSeleccionada, 4);

        // Verificar que no esté ya cancelada
        if ("Cancelada".equalsIgnoreCase(estado)) {
            JOptionPane.showMessageDialog(
                    this,
                    "Esta solicitud ya está cancelada.",
                    "Solicitud cancelada",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        // Confirmar cancelación
        int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "¿Estás seguro de que deseas cancelar esta solicitud de adopción?",
                "Confirmar cancelación",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);

        if (confirmacion == JOptionPane.YES_OPTION) {
            try {
                controlPresentacion.cancelarSolicitudAdopcion(idSolicitud);

                // Actualizar tabla
                cargarSolicitudes();

                JOptionPane.showMessageDialog(
                        this,
                        "Solicitud cancelada exitosamente.",
                        "Cancelación exitosa",
                        JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(
                        this,
                        "Error al cancelar la solicitud: " + e.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }

