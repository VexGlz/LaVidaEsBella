        private void btnGestionarSolicitudesActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnGestionarSolicitudesActionPerformed
                // Crear el panel de solicitudes
                cuaceptarsolicitudes.presentacion.JPSolicitudesCU panelSolicitudes = new cuaceptarsolicitudes.presentacion.JPSolicitudesCU();

                // Obtener la ventana actual
                java.awt.Window window = javax.swing.SwingUtilities.getWindowAncestor(this);
                if (window instanceof javax.swing.JFrame) {
                        javax.swing.JFrame frame = (javax.swing.JFrame) window;

                        // Configurar el listener de inicio para volver a JPAdmin
                        panelSolicitudes.setInicioListener(() -> {
                                JPAdmin adminPanel = new JPAdmin();
                                frame.setContentPane(adminPanel);
                                frame.revalidate();
                                frame.repaint();
                                frame.setTitle("Panel de Administración");
                        });

                        // Reemplazar el contenido por el panel de solicitudes
                        frame.setContentPane(panelSolicitudes);
                        frame.revalidate();
                        frame.repaint();
                        frame.setTitle("Gestión de Solicitudes de Adopción");

                        frame.pack();
                        frame.setLocationRelativeTo(null);
                }
        }// GEN-LAST:event_btnGestionarSolicitudesActionPerformed

