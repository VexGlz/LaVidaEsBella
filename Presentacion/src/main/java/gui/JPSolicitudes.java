    private void configurarTabla() {
        modeloTabla = new DefaultTableModel(
                new Object[] { "ID", "Mascota", "Fecha Solicitud", "Fecha Cita", "Estado" }, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Hacer la tabla de solo lectura
            }
        };
        tablaSolicitudes.setModel(modeloTabla);

        // Ocultar columna ID
        tablaSolicitudes.getColumnModel().getColumn(0).setMinWidth(0);
        tablaSolicitudes.getColumnModel().getColumn(0).setMaxWidth(0);
        tablaSolicitudes.getColumnModel().getColumn(0).setWidth(0);
    }

    /**
     * Carga las solicitudes del usuario en la tabla.
     */
    private void cargarSolicitudes() {
        if (controlPresentacion == null) {
            System.err.println("DEBUG: ControlPresentacion no está configurado");
            return;
        }

        // Limpiar tabla
        modeloTabla.setRowCount(0);

        try {
            List<SolicitudAdopcionDTO> solicitudes = controlPresentacion.obtenerSolicitudesUsuario();

            if (solicitudes == null || solicitudes.isEmpty()) {
                return;
            }

            for (SolicitudAdopcionDTO solicitud : solicitudes) {
                Object[] fila = new Object[5];
                fila[0] = solicitud.getId(); // ID (oculto)
                String nombreMascota = "N/A";
                if (solicitud.getMascota() != null) {
                    nombreMascota = solicitud.getMascota().getNombre() != null ? solicitud.getMascota().getNombre()
                            : solicitud.getMascota().getId();
                }
                fila[1] = nombreMascota;

                // Formatear fecha de solicitud
                java.time.format.DateTimeFormatter formatter = java.time.format.DateTimeFormatter
                        .ofPattern("dd/MM/yyyy HH:mm");

                if (solicitud.getFechaSolicitud() != null) {
                    fila[2] = solicitud.getFechaSolicitud().format(formatter);
                } else {
                    fila[2] = "N/A";
                }

                // Fecha Cita
                if (solicitud.getFechaCita() != null) {
                    fila[3] = solicitud.getFechaCita().format(formatter);
                } else {
                    fila[3] = "Sin Cita";
                }

                fila[4] = solicitud.getEstado() != null ? solicitud.getEstado() : "Pendiente";

                modeloTabla.addRow(fila);
            }

        } catch (Exception e) {
            System.err.println("DEBUG: Excepción en cargarSolicitudes: " + e.getMessage());
            e.printStackTrace();
            JOptionPane.showMessageDialog(
                    this,
                    "Error al cargar solicitudes: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

