    /**
     * Inicializa el modelo de la tabla de citas.
     */
    private void inicializarTabla() {
        modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        modeloTabla.addColumn("ID"); // Oculto
        modeloTabla.addColumn("Fecha");
        modeloTabla.addColumn("Hora");

        tblCitas.setModel(modeloTabla);

        // Ocultar columna ID
        tblCitas.getColumnModel().getColumn(0).setMinWidth(0);
        tblCitas.getColumnModel().getColumn(0).setMaxWidth(0);
        tblCitas.getColumnModel().getColumn(0).setWidth(0);
    }

    /**
     * Carga las citas disponibles desde el controlador.
     */
    private void cargarCitasDisponibles() {
        if (control == null)
            return;

        List<CitaDisponibleDTO> citas = control.obtenerCitasDisponibles();
        modeloTabla.setRowCount(0);

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        for (CitaDisponibleDTO cita : citas) {
            modeloTabla.addRow(new Object[] {
                    cita, // Guardamos el objeto completo en la columna oculta
                    sdf.format(cita.getFecha()),
                    cita.getHora()
            });
        }
    }

    /**
     * Obtiene la cita seleccionada.
     * 
     * @return DTO de la cita seleccionada.
     */
    public CitaDisponibleDTO getCitaSeleccionada() {
        return citaSeleccionada;
    }

