        /**
         * Cierra la ventana de registro y regresa a la ventana de inicio de sesión.
         * 
         * @param evt El evento de acción.
         */
        private void btnRegresarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnRegresarActionPerformed
                // Cerrar ventana de registro y volver al inicio de sesión
                if (frmPadre != null) {
                        control.ControlPresentacion control = new control.ControlPresentacion();
                        control.regresarDesdeRegistro(this, frmPadre);
                } else {
                        // Si no hay padre, simplemente cerrar
                        this.dispose();
                }
        }// GEN-LAST:event_btnRegresarActionPerformed

        /**
         * Valida los datos del formulario y registra un nuevo usuario en el sistema.
         * Muestra mensajes de error si las validaciones fallan.
         * 
         * @param evt El evento de acción.
         */
        private void btnRegitrarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnRegitrarActionPerformed
                // Obtener datos del formulario
                String nombre = TfNom.getText().trim();
                String correo = TfCorreo1.getText().trim();
                String password = new String(PfContra.getPassword());

                // VALIDACIONES

                // 1. Validar nombre
                if (nombre.isEmpty()) {
                        javax.swing.JOptionPane.showMessageDialog(this,
                                        "Por favor ingrese su nombre completo",
                                        "Campo requerido",
                                        javax.swing.JOptionPane.WARNING_MESSAGE);
                        TfNom.requestFocus();
                        return;
                }

                // 2. Validar correo no vacío
                if (correo.isEmpty()) {
                        javax.swing.JOptionPane.showMessageDialog(this,
                                        "Por favor ingrese su correo electrónico",
                                        "Campo requerido",
                                        javax.swing.JOptionPane.WARNING_MESSAGE);
                        TfCorreo1.requestFocus();
                        return;
                }

                // 3. Validar formato de correo
                if (!correo.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$")) {
                        javax.swing.JOptionPane.showMessageDialog(this,
                                        "Por favor ingrese un correo electrónico válido (ejemplo: usuario@dominio.com)",
                                        "Correo inválido",
                                        javax.swing.JOptionPane.WARNING_MESSAGE);
                        TfCorreo1.requestFocus();
                        return;
                }

                // 4. Validar contraseña no vacía
                if (password.isEmpty()) {
                        javax.swing.JOptionPane.showMessageDialog(this,
                                        "Por favor ingrese su contraseña",
                                        "Campo requerido",
                                        javax.swing.JOptionPane.WARNING_MESSAGE);
                        PfContra.requestFocus();
                        return;
                }

                // 5. Validar longitud mínima de contraseña
                if (password.length() < 6) {
                        javax.swing.JOptionPane.showMessageDialog(this,
                                        "La contraseña debe tener al menos 6 caracteres",
                                        "Contraseña muy corta",
                                        javax.swing.JOptionPane.WARNING_MESSAGE);
                        PfContra.requestFocus();
                        return;
                }

                try {
                        // Crear DTO de usuario
                        DTOS.UsuarioDTO usuario = new DTOS.UsuarioDTO();
                        usuario.setContrasena(password);

                        DTOS.InfoPersonalDTO infoPersonal = new DTOS.InfoPersonalDTO();
                        infoPersonal.setNombre(nombre);
                        infoPersonal.setCorreo(correo.toLowerCase()); // Normalizar a minúsculas
                        usuario.setInfoPersonal(infoPersonal);

                        // Registrar usuario
                        control.ControlPresentacion control = new control.ControlPresentacion();
                        control.registrarUsuario(usuario);

                        // Registro exitoso
                        javax.swing.JOptionPane.showMessageDialog(this,
                                        "¡Usuario registrado exitosamente!\nYa puedes iniciar sesión.",
                                        "Registro exitoso",
                                        javax.swing.JOptionPane.INFORMATION_MESSAGE);

                        // Volver a la pantalla de login
                        if (frmPadre != null) {
                                frmPadre.setVisible(true);
                        }
                        this.dispose();

                } catch (Exception e) {
                        // Manejar error específico de correo
                        if (e.getMessage() != null && e.getMessage().startsWith("REGISTRO_EXITOSO_CORREO_FALLIDO")) {
                                javax.swing.JOptionPane.showMessageDialog(this,
                                                "¡Usuario registrado exitosamente!\n\n" +
                                                                "Sin embargo, hubo un problema al enviar el correo de bienvenida:\n"
                                                                +
                                                                e.getMessage().replace(
                                                                                "REGISTRO_EXITOSO_CORREO_FALLIDO: ", "")
                                                                +
                                                                "\n\nPuedes iniciar sesión normalmente.",
                                                "Registro exitoso (Sin Correo)",
                                                javax.swing.JOptionPane.WARNING_MESSAGE);

                                // Volver a la pantalla de login (porque el registro SÍ funcionó)
                                if (frmPadre != null) {
                                        frmPadre.setVisible(true);
                                }
                                this.dispose();
                        } else {
                                // Mostrar error normal
                                javax.swing.JOptionPane.showMessageDialog(this,
                                                e.getMessage(),
                                                "Error de registro",
                                                javax.swing.JOptionPane.ERROR_MESSAGE);
                        }
                }
        }// GEN-LAST:event_btnRegitrarActionPerformed

