    private void cargarDatosMascota() {
        if (mascotaId == null || mascotaId.isEmpty()) {
            System.err.println("No hay ID de mascota para cargar");
            return;
        }

        try {
            gestion.mascotasarchivadas.dtos.MascotaArchivoDTO mascota = controlPresentacion.obtenerDetalles(mascotaId);

            if (mascota != null) {
                tfNombre1.setText(mascota.getNombre());
                tfEspecie1.setText(mascota.getEspecie());
                tfRaza1.setText(mascota.getRaza());
                tfColor1.setText(mascota.getColor());
                tfEdad1.setText(String.valueOf(mascota.getEdad()));
                tfPeso1.setText(String.valueOf(mascota.getPeso()));
                tfCondicion1.setText(mascota.getEstadoSalud());

                if (mascota.getUrlImagen() != null && !mascota.getUrlImagen().isEmpty()) {
                    try {
                        java.io.File imageFile = new java.io.File(mascota.getUrlImagen());
                        if (imageFile.exists()) {
                            javax.swing.ImageIcon icon = new javax.swing.ImageIcon(imageFile.getAbsolutePath());
                            java.awt.Image img = icon.getImage().getScaledInstance(200, 200,
                                    java.awt.Image.SCALE_SMOOTH);
                            lbl_Imagen1.setIcon(new javax.swing.ImageIcon(img));
                            lbl_Imagen1.setText("");
                        }
                    } catch (Exception e) {
                        System.err.println("Error al cargar imagen: " + e.getMessage());
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error al cargar datos: " + e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

